const pentas = [
  {
    modelo: "C",
    tab: "E: |-1-|---|---|-4-|\nA: |-1-|---|---|-4-|\nD: |-1-|---|-3-|---|\nG: |-1-|---|-3-|---|\nB: |---|-2-|---|-4-|\ne: |-1-|---|---|-4-|\n"
  },
  {
    modelo: "A",
    tab: "E: |---|-2-|---|-4-|\nA: |---|-2-|---|-4-|\nD: |-1-|---|---|-4-|\nG: |-1-|---|---|-4-|\nB: |---|-2-|---|-4-|\ne: |---|-2-|---|-4-|\n"
  },
  {
    modelo: "G",
    tab: "E: |-1-|---|---|-4-|\nA: |-1-|---|-3-|---|\nD: |-1-|---|-3-|---|\nG: |-1-|---|-3-|---|\nB: |-1-|---|---|-4-|\ne: |-1-|---|---|-4-|\n"
  },
  {
    modelo: "E",
    tab: "E: |---|-2-|---|-4-|\nA: |-1-|---|---|-4-|\nD: |-1-|---|---|-4-|\nG: |-1-|---|-3-|---|\nB: |---|-2-|---|-4-|\ne: |---|-2-|---|-4-|\n"
  },
  {
    modelo: "D",
    tab: "E: |---|-1-|---|-3-|---|\nA: |---|-1-|---|-3-|---|\nD: |---|-1-|---|-3-|---|\nG: |-1-|---|---|-4-|---|\nB: |---|-1-|---|---|-4-|\ne: |---|-1-|---|-3-|---|\n"
  }
]

function sortearPenta() {
  const result = pentas
    .map(penta => ({ item: penta, random: Math.random() }))
    .sort((a, b) => a.random - b.random)
    .map(item => item.item)

  return `Pentatonica modelo: ${result[0].modelo}\n\n${result[0].tab}`
}

console.log(sortearPenta())
